def normalize_lang(lang: str | None) -> str:
    if not lang:
        return DEFAULT_LANG
    value = lang.strip().lower()
    if value.startswith("en"):
        return "en"
    return DEFAULT_LANG


def tr(lang: str | None, key: str, **kwargs: Any) -> str:
    resolved_lang = normalize_lang(lang)
    template = TEXT[resolved_lang].get(key, TEXT[DEFAULT_LANG].get(key, key))
    return template.format(**kwargs)
